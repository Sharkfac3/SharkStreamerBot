/**
 * overlay.spawn
 * Spawn a PNG/JPG/GIF asset at a canvas position.
 *
 * Asset ID strategy:
 * - Ephemeral/one-off assets (alerts, reactions): UUID v4 generated by sender
 * - Persistent/named assets (HUD widgets, meters): human-readable slug
 *   e.g. "chaos-meter", "lotat-hud", "duck-counter"
 *   Named assets can be moved/animated/removed by referencing the same slug.
 */
export interface OverlaySpawnPayload {
  /**
   * Unique asset identifier.
   * Ephemeral: UUID v4. Persistent: human-readable slug (kebab-case).
   * Spawning an asset with an existing ID replaces that asset.
   */
  assetId: string;
  /**
   * Path or URL to the image/GIF asset.
   * Relative paths resolve from packages/overlay/public/ in both dev and prod.
   */
  src: string;
  position: CanvasPosition;
  /** Display width in pixels. If omitted, height must be provided; aspect ratio preserved. */
  width?: number;
  /** Display height in pixels. If omitted, width must be provided; aspect ratio preserved. */
  height?: number;
  /** Phaser depth (z-index). Higher values render on top. Defaults to 0. */
  depth?: number;
  /** Animation to play as the asset appears. Defaults to "none". */
  enterAnimation?: AnimationPreset;
  /** Duration of entry animation in milliseconds. Defaults to 300. */
  enterDuration?: number;
  /**
   * Auto-remove the asset after this many milliseconds.
   * Omit for persistent assets that stay until explicitly removed.
   */
  lifetime?: number;
  /** Exit animation to play when the asset is auto-removed via lifetime. */
  exitAnimation?: AnimationPreset;
  /** Duration of exit animation in milliseconds. Defaults to 300. */
  exitDuration?: number;
}

/**
 * overlay.move
 * Tween an existing asset to a new canvas position.
 */
export interface OverlayMovePayload {
  assetId: string;
  position: CanvasPosition;
  /** Tween duration in milliseconds. 0 = instant snap. Defaults to 300. */
  duration?: number;
}

/**
 * overlay.animate
 * Apply or change an animation on an existing asset without moving it.
 */
export interface OverlayAnimatePayload {
  assetId: string;
  animation: AnimationPreset;
  /** Animation duration in milliseconds. Defaults to 300. */
  duration?: number;
}

/**
 * overlay.remove
 * Remove a specific asset from the canvas.
 */
export interface OverlayRemovePayload {
  assetId: string;
  /** Exit animation to play before the asset is destroyed. Defaults to "none". */
  exitAnimation?: AnimationPreset;
  /** Duration of exit animation in milliseconds. Defaults to 300. */
  exitDuration?: number;
}

/**
 * overlay.clear
 * Remove all assets from the canvas, with optional prefix filter.
 * Use sparingly — prefer overlay.remove for targeted cleanup.
 */
export interface OverlayClearPayload {
  /**
   * If provided, only assets whose IDs start with this prefix are removed.
   * Example: "lotat-" removes all assets with IDs beginning with "lotat-".
   * If omitted, ALL assets are cleared.
   */
  assetIdPrefix?: string;
  /** Exit animation applied to all removed assets. Defaults to "none". */
  exitAnimation?: AnimationPreset;
  /** Duration of exit animation in milliseconds. Defaults to 300. */
  exitDuration?: number;
}

/**
 * overlay.audio.play
 * Play an MP3 sound through the Phaser audio system.
 * OBS browser source must have audio enabled for sounds to reach stream output.
 */
export interface OverlayAudioPlayPayload {
  /**
   * Unique sound instance ID — used to stop this specific sound later.
   * Use a UUID v4 for one-shot sounds; a stable slug for looping ambient audio.
   */
  soundId: string;
  /**
   * Path or URL to the MP3 file.
   * Relative paths resolve from packages/overlay/public/.
   */
  src: string;
  /** Volume from 0 (silent) to 1 (full). Defaults to 1. */
  volume?: number;
  /** Whether to loop the sound indefinitely. Defaults to false. */
  loop?: boolean;
}

/**
 * overlay.audio.stop
 * Stop a playing sound by its instance ID.
 */
export interface OverlayAudioStopPayload {
  /**
   * Sound instance ID to stop.
   * If omitted, ALL currently playing sounds are stopped.
   */
  soundId?: string;
}

