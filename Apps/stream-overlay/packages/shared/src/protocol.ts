/**
 * @stream-overlay/shared — Message Protocol
 *
 * The canonical TypeScript contract for all messages that flow through the broker.
 * Every service in the ecosystem (Streamer.bot, overlay, future web app) must
 * conform to these types.
 *
 * - No runtime logic lives here — types and interfaces only.
 * - Payload shapes are derived from session-lifecycle.md and json-schema.md.
 *   Do not invent fields that don't exist in those docs.
 * - Topic strings live in topics.ts — import from there, never hardcode.
 */

// ============================================================
// CORE ENVELOPE
// ============================================================

/**
 * Every message through the broker must conform to this shape.
 * The broker routes on `topic`; it does not inspect `payload`.
 */
export interface BrokerMessage<P = unknown> {
  /** UUID v4 — unique per message, generated by the sender before publishing */
  id: string;
  /** Dot-notation topic string, e.g. "overlay.spawn" or "lotat.session.start" */
  topic: string;
  /** Stable human-readable sender name, e.g. "streamerbot", "overlay" */
  sender: string;
  /** Unix epoch milliseconds — Date.now() at time of send */
  timestamp: number;
  /** Topic-specific payload — shape defined by the interfaces below */
  payload: P;
}

// ============================================================
// CLIENT REGISTRATION HANDSHAKE
//
// Flow: client connects → sends ClientHello → broker responds ClientWelcome
// All clients (Streamer.bot, overlay, future apps) use the same handshake.
// ============================================================

/**
 * Sent by a client immediately after the WebSocket connection opens.
 * Must be the first message sent on a new connection.
 */
export interface ClientHello {
  type: "client.hello";
  /**
   * Human-readable, stable client name.
   * Used in system.client.connected/disconnected events and broker logs.
   * Must be consistent across reconnects (not randomly generated each time).
   * Known values: "streamerbot" | "overlay"
   */
  name: string;
  /**
   * Topic patterns this client wants to receive.
   * Supports single-level wildcards: "lotat.*" matches "lotat.session.start"
   * but NOT "lotat.visual.spawn" (that would require a separate "lotat.visual.*").
   * Use wildcard constants from topics.ts to avoid typos.
   */
  subscriptions: string[];
}

/**
 * Sent by the broker in response to a valid ClientHello.
 * Confirms the client is registered and subscriptions are active.
 */
export interface ClientWelcome {
  type: "client.welcome";
  /** Broker-assigned unique ID for this connection session */
  clientId: string;
  /** Human-readable names of all currently connected clients (excluding this one) */
  connectedClients: string[];
}

