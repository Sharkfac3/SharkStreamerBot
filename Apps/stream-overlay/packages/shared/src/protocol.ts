/** 2D position on the 1920×1080 overlay canvas (pixels) */
export interface CanvasPosition {
  x: number;
  y: number;
}

/**
 * Named animation preset — interpreted by the Phaser overlay renderer.
 * New presets must be supported in the overlay before being sent by Streamer.bot.
 */
export type AnimationPreset =
  | "fade-in"
  | "fade-out"
  | "slide-in-left"
  | "slide-in-right"
  | "slide-in-top"
  | "slide-in-bottom"
  | "bounce"
  | "shake"
  | "pulse"
  | "spin"
  | "none";

