// LOTAT TOPICS — session lifecycle events
//
// Topic sequence mirrors the session-lifecycle.md stage machine:
//   idle → join_open → node_intro → [commander_open?] → [dice_open?]
//       → decision_open → decision_resolving → node_intro → ... → ended → idle
//
// Streamer.bot publishes these. The overlay subscribes to render LotAT UI.
// ============================================================

/**
 * lotat.session.start
 * Stage transition: idle → join_open
 * Signals that a new LotAT session has been initialized and the join phase
 * is about to open. Overlay should prepare any LotAT persistent UI.
 */
export interface LotatSessionStartPayload {
  /** Session-scoped unique ID (UUID v4) — included in all subsequent LotAT events */
  sessionId: string;
  /** Matches story_id from the loaded story JSON */
  storyId: string;
  /** Story display title */
  title: string;
}

/**
 * lotat.session.end
 * Stage transition: ended → idle
 * Session is fully torn down. Overlay should remove all LotAT UI.
 */
export interface LotatSessionEndPayload {
  sessionId: string;
  /** How the session ended */
  reason: "ending-reached" | "zero-join" | "zero-votes" | "fault-abort";
  /**
   * Authored end_state from the ending node.
   * Only present when reason is "ending-reached".
   */
  endState?: "success" | "partial" | "failure";
}

/**
 * lotat.join.open
 * Stage: join_open is now active.
 * Join window is open; chat may type !join.
 */
export interface LotatJoinOpenPayload {
  sessionId: string;
  /** Join window duration in seconds. Fixed at 120 in v1. */
  windowSeconds: number;
}

/**
 * lotat.join.update
 * Stage: join_open — roster changed.
 * A viewer successfully joined; participant count increased.
 */
export interface LotatJoinUpdatePayload {
  sessionId: string;
  /** Lowercased username/login of the viewer who just joined */
  username: string;
  /** Running participant count after this join */
  participantCount: number;
}

/**
 * lotat.join.close
 * Stage: join_open → node_intro (or ended if zero joins).
 * Join phase closed; roster is now frozen.
 */
export interface LotatJoinClosePayload {
  sessionId: string;
  /** Final frozen participant count. 0 = zero-join path (session will end). */
  participantCount: number;
}

/**
 * lotat.node.enter
 * Stage: node_intro
 * Engine has entered a story node. Overlay should render read_aloud, crew_focus,
 * and prepare any mechanic UI (dice or commander) that precedes voting.
 *
 * Payload fields are derived strictly from the authored story JSON schema.
 * Do not add fields that don't exist in json-schema.md.
 */
export interface LotatNodeEnterPayload {
  sessionId: string;
  /** Matches node_id from story JSON */
  nodeId: string;
  nodeType: "stage" | "ending";
  /** Ship section for this node */
  shipSection: string;
  /** Short internal node title */
  title: string;
  /** Stream-facing narration block — read aloud by operator */
  readAloud: string;
  /** Production audio hint, or null if none */
  sfxHint: string | null;
  /** Crew member spotlighted at this node */
  crewFocus: {
    commander: string | null;
    squadMember: string | null;
  };
  /** Chaos increase applied when entering this node */
  chaosDelta: number;
  /**
   * Present only when dice_hook.enabled = true on this node.
   * Overlay uses this to render the dice roll UI before voting opens.
   */
  diceHook?: {
    purpose: string;
    rollWindowSeconds: number;
    successThreshold: number;
  };
  /**
   * Present only when commander_moment.enabled = true on this node.
   * Overlay uses this to render the commander prompt before voting opens.
   */
  commanderMoment?: {
    commander: string;
    prompt: string;
    windowSeconds: number;
  };
  /**
   * Choices available at this node — for overlay display only.
   * Empty array for ending nodes.
   */
  choices: Array<{
    choiceId: string;
    label: string;
    command: string;
  }>;
  /** null for stage nodes; "success" | "partial" | "failure" for ending nodes */
  endState: "success" | "partial" | "failure" | null;
}

/**
 * lotat.vote.open
 * Stage: decision_open
 * Decision window is now live; joined participants may vote.
 */
export interface LotatVoteOpenPayload {
  sessionId: string;
  nodeId: string;
  /** Active choices for this decision window */
  choices: Array<{
    choiceId: string;
    label: string;
    command: string;
  }>;
  /** Decision window duration in seconds. Fixed at 120 in v1. */
  windowSeconds: number;
  /** Number of joined participants in the frozen roster */
  participantCount: number;
}

/**
 * lotat.vote.cast
 * Stage: decision_open — a valid vote was received.
 * Sent each time an eligible participant casts a vote.
 * Overlay can use this to update a live vote tally display.
 */
export interface LotatVoteCastPayload {
  sessionId: string;
  nodeId: string;
  /** Lowercased username of the voter */
  username: string;
  /** The command they voted with (e.g. "!scan") */
  command: string;
  /** Running vote totals after this cast — key is command string, value is vote count */
  voteTotals: Record<string, number>;
  /** How many of the frozen roster have now voted */
  votedCount: number;
  /** Total frozen roster size (denominator for early-close check) */
  participantCount: number;
}

/**
 * lotat.vote.close
 * Stage: decision_resolving (or ended if zero votes)
 * Voting is closed and the winner has been resolved.
 */
export interface LotatVoteClosePayload {
  sessionId: string;
  nodeId: string;
  /** Winning command string, or null if zero valid votes (session ends unresolved) */
  winningCommand: string | null;
  winningChoiceId: string | null;
  /** Authored result_flavor text for the winning choice, or null if unresolved */
  resultFlavor: string | null;
  /** Final vote totals */
  voteTotals: Record<string, number>;
  /** Next node to enter. Null when session is ending (ending node or unresolved). */
  nextNodeId: string | null;
}

/**
 * lotat.dice.open
 * Stage: dice_open
 * Dice roll window is now live; any viewer may type !roll.
 */
export interface LotatDiceOpenPayload {
  sessionId: string;
  nodeId: string;
  /** Authored purpose string — what the roll is for */
  purpose: string;
  /** A roll >= this value is a success */
  successThreshold: number;
  /** Window duration in seconds (authored per-node via dice_hook.roll_window_seconds) */
  windowSeconds: number;
}

/**
 * lotat.dice.roll
 * Stage: dice_open — a viewer used !roll.
 * Sent for every roll attempt while the window is open.
 * Overlay can display each roll result to chat.
 */
export interface LotatDiceRollPayload {
  sessionId: string;
  nodeId: string;
  /** Lowercased username who rolled */
  username: string;
  /** Random value 1–100 */
  rollValue: number;
  successThreshold: number;
  /** True when rollValue >= successThreshold (this roll closes the window on success) */
  isSuccess: boolean;
}

/**
 * lotat.dice.close
 * Stage: dice_open resolved — dice window is over.
 * Overlay should display the authored outcome text, then prepare for voting.
 */
export interface LotatDiceClosePayload {
  sessionId: string;
  nodeId: string;
  outcome: "success" | "failure";
  /** Winning roll value — only present when outcome is "success" */
  winningRoll?: number;
  /** Username of the successful roller — only present when outcome is "success" */
  winningUsername?: string;
  /**
   * Authored outcome text to surface (success_text or failure_text from story JSON).
   * Narrative-only in v1 — does not affect branching or chaos.
   */
  outcomeText: string;
}

/**
 * lotat.commander.open
 * Stage: commander_open
 * Commander moment window is now live. Only the assigned commander user's
 * input commands are accepted during this window.
 */
export interface LotatCommanderOpenPayload {
  sessionId: string;
  nodeId: string;
  /** Which commander owns this moment (matches commander_moment.commander in story JSON) */
  commander: string;
  /** Authored call-to-action text for the commander window */
  prompt: string;
  /** Window duration in seconds (authored per-node via commander_moment.window_seconds) */
  windowSeconds: number;
}

/**
 * lotat.commander.close
 * Stage: commander_open resolved — commander moment is over.
 * Narrative-only in v1 — outcome does not affect branching, chaos, or vote eligibility.
 */
export interface LotatCommanderClosePayload {
  sessionId: string;
  nodeId: string;
  /** success = assigned commander responded; skipped = timeout or no assigned commander */
  outcome: "success" | "skipped";
  /**
   * Authored success_text from story JSON.
   * Only present when outcome is "success".
   */
  successText?: string;
}

/**
 * lotat.chaos.update
 * Emitted each time the chaos total changes (on every node entry where chaosDelta > 0).
 * Overlay uses this to update any chaos meter display.
 */
export interface LotatChaosUpdatePayload {
  sessionId: string;
  /** New cumulative chaos total for this session */
  chaosTotal: number;
  /** Delta applied at the current node (from chaos.delta in story JSON) */
  delta: number;
}

