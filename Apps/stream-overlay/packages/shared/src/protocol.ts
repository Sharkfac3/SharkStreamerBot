/**
 * Helper: produce a fully typed BrokerMessage for a known topic.
 *
 * @example
 * const msg: TypedBrokerMessage<"overlay.spawn"> = {
 *   id: crypto.randomUUID(),
 *   topic: TOPICS.OVERLAY_SPAWN,
 *   sender: CLIENT_NAMES.STREAMERBOT,
 *   timestamp: Date.now(),
 *   payload: { assetId: "pedro-alert", src: "pedro.gif", position: { x: 100, y: 100 } },
 * };
 */
export type TypedBrokerMessage<T extends keyof TopicPayloadMap> =
  BrokerMessage<TopicPayloadMap[T]>;
